'''17.Mostrar els n primers primers'''

def is_prime(number):
    if number < 2:
        return False
    for i in range(2, int(number ** 0.5) + 1):
        if number % i == 0:
            return False
    return True

def first_n_primes(n):
    primes = []
    num = 2
    while len(primes) < n:
        if is_prime(num):
            primes.append(num)
        num += 1
    print(primes)

entrada = int(input("Ingresa un numero: "))
first_n_primes(entrada)
